<p align="justify">Islamic University of Technology (IUT) is a subsidiary organ of the Organisation of Islamic Cooperation (OIC), representing fifty-seven member countries from Asia, Africa, Europe and South America. It is dedicated to the development of human resources of the Islamic Ummah in the fields of engineering, technology and technical education and stands as the most visible demonstration of the Islamic Solidarity and Joint Islamic Action under the Makkah-Al-Mukarrarnah Declaration.</br></br>

This unique educational institution was initially established as the Islamic Centre for Technical and Vocational Training and Research (ICTVTR), in pursuance of a resolution of the Ninth Islamic Conference of Foreign Ministers (ICFM), held in Dakar, Senegal in 1978. The foundation stone was laid on 27 March 1981 on the 30-acre land donated by the Government of the People's Republic of Bangladesh to the OIC. ICTVTR was renamed as the Islamic Institute of Technology (IIT), by the Seventh Islamic Summit and the 22nd ICFM, held in Casablanca, Morocco on 10-15 December, 1994. In appreciation of the activities of IIT, the 28th session of the ICFM, held in Bamako, Republic of Mali on 25-27 June, 2001, renamed IIT as the Islamic University of Technology (IUT).</br></br>

There  are  around  1200  students  from  Bangladesh  and  OIC  member  countries  in IUT, and  all  of them are availing  full  tuition  and  accommodation  scholarship  funded  by  the  OIC. The finest and  the best  students  are  selected  for  the  scholarship  program  based  on  their  performance  in  the admission test, as well as in higher  secondary or equivalent  examination results in the respective OIC  member countries.  IUT offers Diploma, B.Sc. Engg., M.Sc. Engg. and Ph.D. in  the  departments  of  Electrical  and Electronic Engineering (EEE), Computer  Science  and  Engineering  (CSE),  Mechanical and Chemical Engineering (MCE),  Civil and  Environmental  Engineering  (CEE)  and  Technical  and  Vocational  Education (TVE).  IUT started offering regular courses since 1986 and successfully completed 27 academic years till 2013.</br></br>
	
