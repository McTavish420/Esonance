                  <div class="accordion-heading">
                    <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseFour">
                      Arena Specification for Autonomous Task
                    </a>
                  </div>
                  <div id="collapseFour" class="accordion-body collapse" style="height: 0px;">
                    <div class="accordion-inner"></br></br>
					 Competition arena will be made of a flat wood platform, with a maximum size<br>
approximately 850cm x 300cm.<br>
&bull; The arena consists of a black semi-gloss painted road (track) of 30 cm wide, with<br>
a centered white line of 2.5±0.2 cm wide.<br>
&bull; The starting and finishing Zone will be defined separately by 30 cm × 30 cm white<br>
colored square area in the arena.<br>
&bull; There will be check marks at different position of the track.<br>
&bull; There will be a Gap on the track for 15±5 cm, and may be placed in a curve or<br>
straight portion of the track.<br>
&bull; There will be &ldquo;T&rdquo;, &ldquo;Y&rdquo; and &ldquo;+&rdquo; shape intersections.<br>
&bull; The track will have arcs with different curvatures. Minimal curve diameter will be<br>
10 cm.<br>
&bull; Sharp angles may occur, but will not be less than 30°.<br>
&bull; Different obstacles with the discontinuity of the line (maximum dimension of the<br>
obstacle is approximately 20 cm x 10 cm x 5 cm; L x W x H) will be placed on the<br>
track. The robot has to drive around these obstacles to reach the continuation of<br>
the line.<br>
&bull; The number of obstacles will be no more than 4.<br>
&bull; The track will contain shortcuts that might or might not contain obstacles of the<br>
above mentioned dimensions.<br>
&bull; Bridge/hill will be placed on the track. The length will be at least 40cm. Height will<br>
be at most 15cm above the ground and the inclined angle will be maximum 300.<br>
The black line will be continue on the hill/bridge.<br>
&bull; Position of check points, gaps, obstacles and hill/bridge may be anywhere on the<br>
track.<br>
&bull; Lighting levels are unpredictable, so the robot must be able to operate over a<br>
wide range of lighting conditions.<br>
&bull; There will be a final checkpoint on crossing which the bridge for the 2nd task will be<br>
established automatically.<br>
&bull; Keeping the basic specifications of arena fixed, there will be modifications in<br>
arena for 3 stages with increasing difficulties.


