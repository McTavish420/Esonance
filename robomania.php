                    <div class="accordion-inner"></br></br>
				 The competition arena for the 2nd task is made out of flat wood platform, with a<br>
maximum size of approximately 850cm X 60cm.<br>
&bull; There are two ramps on either side of the arena of angle 30±5 degree.<br>
&bull; The starting and the unloading zones will be marked by 50cm X 60cm mark as<br>
&ldquo;Starting&rdquo; and &ldquo;Unloading Zones&rdquo;.<br>
&bull; The arena will be divided in different zones. Each zone has fixed width of 60cm but<br>
varied length. Each zone has 1.5±0.2 cm height variation with respect to its adjacent<br>
zones.<br>
&bull; The arena will have a zone defined by 40cm X 60cm filled with water, over which a<br>
bridge of 35cm wide will be established after the autonomous robot completes its<br>
task.<br>
&bull; The arena will have a zone defined by 90cm X 60cm which will consist of small stones<br>
and gravels, however the height of this zone will not be uniform. Height will be varied<br>
in between 8cm to 10cm. The manual robot needs to cross this zone without the aid<br>
of any bridges.<br>
&bull; The manual robot needs to pick up objects of dimension 4cm x 4cm x 4cm<strong>, </strong>weight<br>
100±50 gm. which might be placed at any position along the track.<br>
&bull; Maximum number of objects may be 4.<br>
&bull; Objects will be placed anywhere in the arena. The manual robot should pick up the<br>
objects simultaneously and unload them simultaneously at the unloading zone.<br>
&bull; Object unloaded outside the vicinity of the unloading zone will not be counted.<br>
