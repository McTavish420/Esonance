                      Unmanned Rescue Mission (URM) at a glance
                    </a>
                  </div>
                  <div id="collapseOne" class="accordion-body in collapse" style="height: auto;">
                    <div class="accordion-inner">
                   <strong>Unmanned Rescue Mission (URM) </strong>is the biggest event of &lsquo;ESONANCE 2015&rsquo; featuring a<br>
robotic competition on &ldquo;Rescue Operation during natural Disaster&rdquo;. Here, participants<br>
have to solve a scaled down version of a problem arena depicting disaster situation with<br>
exclusive obstacles and difficulties.<br>
Delivering aid, including food, clean water, fuel and medical supplies to places during<br>
natural disaster such as Earthquake, becomes a difficult task.<br>
Transporting bulk materials over uneven and rough terrain, in tight spaces, and over long<br>
distances is often required to help those in need.<br>
While several frequent tremors were already felt in different regions of Bangladesh, the<br>
country is far from being completely safe from massive Earthquake threats.<br>
Keeping these situations in mind and its possible after effects, this year&rsquo;s challenge is to<br>
design and develop a scaled-down version of a transporter capable of delivering<br>
aid/materials, which will be guided by, at most, one person.<br>
The contestants need to design <strong>two </strong>robots in this competition: an <strong>autonomous line</strong><br>
<strong>follower </strong>and a <strong>transporter</strong>. Each robot will be assigned to perform separate task in<br>
separate arena (line follower arena and Transporter arena).<br>
1. <strong>Autonomous line follower: </strong>A small <strong>autonomous robot </strong>has to follow a line in<br>
line follower arena under various conditions (e.g. round corners, sharp<br>
corners, crossovers, line obstacles etc.). This line follower robot will be used to<br>
unlock different aids/supports to resolve exclusive obstacles (e.g. bridge,<br>
road block, short cut etc.) for smooth travel of the <strong>transporter </strong>in transporter<br>
arena.<br>
2. <strong>Transporter: </strong>A manually controlled, robot which will be capable of<br>
transporting objects of different shapes and sizes to the final unloading zone<br>
in the transporter arena.<br>
<strong>The manual bot should be wirelessly controlled.</strong><br>
<strong>As for example</strong>, in line follower arena, the autonomous line follower robot crosses some<br>
  obstacles by following a line and finishes predefined checkpoints (tasks). The successful<br>
  completion of those tasks will allow the team to establish a bridge in transporter arena<br>
  for the manually controlled robot (transporter) to cross a zone filled with water.<br>
  Any team failing to perform the tasks to trigger the bridge by the autonomous robot will<br>
  not be able to perform the other task that will involve the manual robot.<br>
  There are 3 stages, each of an increasing level of complexity.<br>
  <strong>– Qualifying Stage</strong><br>
  <strong>– Knock out Stage</strong><br>
  <strong>– Final Stage</strong><br>
  &bull; Top 10 teams in qualifying stage will be eligible for Knock out stage.*<br>
  &bull; Top 5 teams in Knock out stage will be eligible for Final stage.*<br>
  &bull; Top 3 teams will be selected as Champion, 1st runners up, 2nd runners up.<br>
   The competition is open to all the students of school, college and undergraduate<br>
  students of all universities.<br>
   The participant team must register on the website of the competition.<br>
  *<em>No. of eligible team in this stage will be decided on the competition day based on the number</em><br>
  <em>of participant team.</em><br>

