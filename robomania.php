                  <div class="accordion-heading">
                    <a class="accordion-toggle" data-toggle="collapse" data-parent="#accordion2" href="#collapseSeven">
                      Competition Rules
                    </a>
                  </div>
                  <div id="collapseSeven" class="accordion-body collapse" style="height: 0px;">
                    <div class="accordion-inner">
                       Team must place both robots in their respective position and make ready<br>
for run within 3 minutes after calling them to come to the arena. Otherwise<br>
team will be disqualified.<br>
&bull; The autonomous robot will be placed at a pre-assigned starting point and<br>
may be started by hand when directed by the judge.<br>
&bull; The manual robot will be placed at the pre-assigned starting point and will<br>
only be allowed to start once the autonomous robot crosses the final<br>
checkpoint.<br>
&bull; The Autonomous Robot must follow a line from the beginning to the end as<br>
fast as it can and reach the final checkpoint.<br>
&bull; The manual robot should make a onetime run and proceed to the<br>
unloading zone. It picks up each object on the way and unloads them at<br>
the unloading zone.<br>
&bull; Time is measured from crossing the start line by the autonomous line follower<br>
until the manual transporter robot unloads the last object. A robot is<br>
considered to have crossed the line when the forward most part of the<br>
robot contacts or crosses over the line.<br>
&bull; Time will be measured by an electronic gate system or by a judge with a<br>
stopwatch, based on the availability of equipment. In either case the<br>
recorded time will be final.<br>
&bull; The autonomous robot must follow the line fully autonomous without any<br>
human intervention.<br>
&bull; No wireless communication or external data feed to the autonomous robot<br>
is allowed. This will consider as disqualification from the competition.<br>
&bull; The autonomous robot may use shortcut tactics to complete the course<br>
faster by following the line. But the robot must go through every checkpoints<br>
including the final checkpoint. Otherwise, the robot will be disqualified. No<br>
shortcut tactics for the manual task is available.<br>
&bull; The autonomous robot is placed by at the start and should attend the end<br>
point. Any other case, the robot is disqualified.<br>
&bull; For the manual task, the robot should reach the unloading zone. Otherwise<br>
the robot will be disqualified.<br>
<!--EndFragmen>><!--EndFragment-->
The objects must be unloaded in the specific area defined as the unloading<br>
  zone. Objects unloaded outsize the unloading zone will not be considered.<br>
  &bull; The start and the unloading zone can be on either side of the arena, and<br>
  would be revealed on the day of the competition.<br>
  &bull; In competition, only 5 attempts or restarts of the autonomous robot are<br>
  authorized.<br>
  &bull; If the autonomous robot loses the line, it must restart from the last<br>
  checkpoint. In this case, the timer will remain running during restart and a<br>
  penalty will be pointed.<br>
  &bull; In competition, only 3 attempts or restarts of the manual robot are allowed.<br>
  In this case, the timer will remain running and a penalty will be pointed.<br>
  &bull; After crossing the bridge, no restart is allowed for manual robot.<br>
  &bull; If any of the robots steps off the arena it will be disqualified.<br>
  &bull; The judges can ask for an explanation of any mechanism on the robot and<br>
  there would be an immediate disqualification of defaulters of any kind.<br>





