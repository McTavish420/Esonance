<p align="justify"> <strong>&#39; ESONANCE 2015 &#39;</strong> is going to be organized by the Department of Electrical and Electronic Engineering (EEE), the largest department of Islamic University of Technology (IUT). The vision of this event is to bring together students from different institutions and universities across Bangladesh in a delightful environment that will be competitive as well as festive. The participation of students will open up a platform for idea sharing where they can contribute to and benefit from. In this present age, we believe that innovative ideas do not stay confined to any specific group. We expect that students will participate regardless of their disciplines to generate some collaborative, multi-brained contributions. </br></br></br>
 
While most programs of this type have particular themes such as robotics and project showcasing, the aim of this event is a more comprehensive approach. We intend to provide opportunities for competition as well as for sharing of ideas and fostering connections between students of science and engineering from major institutions in the country.</br></br>
 
The next generation of engineers is ready to go, and in a developing country like Bangladesh, there has never been a strong platform for them to capitalize on. Even in schools and colleges, students dream of building their own robots or creating their own machines. &#39; ESONANCE 2015 &#39; is designed to accommodate these enthusiastic innovators regardless of their disciplines. Programs that involve advertising for technical product, idea selling and business case-studies will help connect people having technical knowledge with those having the business know-how, which is essential for success in today's world. We sincerely intend to provide a platform that will be a perfect blend of competition and festivity. It will be a superb opportunity for students, faculties and guests to interact with the huge pool of international participants from 57 different OIC countries, studying currently in Islamic University of Technology (IUT).</br></br>
 
Alongside organizing the program, we plan to release a souvenir publication, documenting the event and promoting the ideas that inspire us. The aim is to publish 1000 copies that will be distributed among the participants, students, faculty members of the country&#39;s major universities, ambassadors and their representatives in different embassies of OIC countries present in Bangladesh. </p></br></br>


