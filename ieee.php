<p align="justify">The Institute of Electrical and Electronics Engineers (IEEE) is the largest technical organization in the world. It is a global platform that has created a network of engineers around the world. It has successfully provided its members with a unique opportunity of sharing their ideas, knowledge and expertise. Every year it arranges worldwide conferences and festivals with a theme to evolve technological solution of real life problems and make the world a better place for humanity. Certainly it reflects its theme “Advancing Technology for Humanity”.</br></br>

IEEE has 2,516 student branches at colleges and universities in 80 countries to create a bridge between the faculties, students and engineering professionals. Islamic University of Technology is no exception to that. IEEE IUT Student Branch (IEEE IUT SB) is one of the oldest in Bangladesh. In 1999 the student branch was created, and since then it has carried out different activities and organized various seminars, technical tours, tutorial sessions and engineering competitions for the benefit of its members. IEEE IUT SB represents IUT in different IEEE events such as IEEE Congress, IEEE day celebration, IEEE Annual Dinner and IEEE Paper Competition Contests.</br></br>

The student branch is currently headed by a fresh executive committee that assumed its responsibilities in December 2013. In 2014, the branch has organized a number of successful events including fresher’s reception, membership drive and a seminar on higher studies abroad. Now IEEE IUT SB expands their horizon by partnering the organization of ‘ESONANCE 2014’. IEEE IUT SB believes that this big event goes with the aim and spirit of IEEE and fully supports the event. IEEE IUT SB would like to be a part of this success story and guarantees that the technological evolution from this event will make an impact in advancing Humanity.</p> </br></br>


